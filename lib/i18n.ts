export function translate(locale: UiLocale, path: string): string {
  const primary = getMessage(messages[locale], path);
  if (primary !== undefined) {
    return primary;
  }
  const fallback = getMessage(messages.en, path);
  if (fallback !== undefined) {
    return fallback;
  }
  return path;
}

/** Localized category label for UI (DB slug stays English). */
export function categoryLabelForLocale(slug: string, locale: UiLocale): string {
  const key = `category.${slug}`;
  const tr = translate(locale, key);
  if (tr !== key) {
    return tr;
  }
  return slug;
}
