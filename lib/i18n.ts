/** UI locale persisted in Supabase Auth user_metadata as `ui_locale`. */
export type UiLocale = "en" | "ko";

export function getBrowserLocale(): UiLocale {
  if (typeof navigator === "undefined") {
    return "en";
  }
  return navigator.language.toLowerCase().startsWith("ko") ? "ko" : "en";
}

export function normalizeUserLocale(raw: unknown): UiLocale {
  if (raw === "ko") {
    return "ko";
  }
  return "en";
}

export function speechRecognitionLang(locale: UiLocale): string {
  return locale === "ko" ? "ko-KR" : "en-US";
}

export function intlLocaleForUi(locale: UiLocale): string {
  return locale === "ko" ? "ko-KR" : "en-US";
}

type MessageTree = { [key: string]: string | MessageTree };

function getMessage(tree: MessageTree, path: string): string | undefined {
  const parts = path.split(".").filter(Boolean);
  let cur: string | MessageTree | undefined = tree;
  for (const p of parts) {
    if (cur === undefined || typeof cur === "string") {
      return undefined;
    }
    cur = cur[p];
  }
  return typeof cur === "string" ? cur : undefined;
}

